days = ['Monday','Tuesday','Wednesday','Thursday','Friday','Saturday','Sunday']
for day in days:
	if day == 'Monday':
		print(day + " I eat apple.")
	elif day =='Tuesday':
		print(day + " I eat banana.")
	elif day == 'Wednesday':
		print(day + " I eat tomato.")
	elif day == 'Thursday':
		print(day + " I eat potato.")
	elif day == 'Friday':
		print(day + " I eat rice.")
	elif day == 'Saturday':
		print(day + " I eat egg.")
	else:
		print(day + " I eat meat.")
	
